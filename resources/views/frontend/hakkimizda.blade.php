<h2>Hakkımızda</h2>
<p>2008 yılında şarküteri alanında faaliyet göstermeye başladık.</p>

<p>2014 yılına kadar şarküteri alanında başarılı bir perakendecilik gösteren firmamız, 2014 yılında Dede ve Babamızdan edindiğimiz tecrübeler ile Eryiğitler Tereyağı adı altında Tereyağı üretimine başladık.</p>

<p>Üstün teknolojiye sahip tesisimizde en doğal tereyağını sizlere ulaştırmak için tüm gayretimizle çalışmalarımıza hız kesmeden devam etmekteyiz.</p>

<p>Hedefimiz insanlarımıza sağlıklı ve doğal ürünler sunmak.</p>